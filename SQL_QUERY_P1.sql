SELECT 
EXTRACT(YEAR FROM sale_date) as year,
EXTRACT(MONTH FROM sale_date) as month,
avg(total_sale),
RANK() OVER(PARTITION BY EXTRACT(YEAR FROM sale_date) ORDER BY avg(total_sale)DESC)
as Rank
FROM retail_sales
GROUP BY year, month)
AS t1 WHERE Rank = 1;

