--Q10.WRITE A SQL QUERY TO FIND EACH SHIFT AND NUMBER OF ORDERS IN EACH SHIFT
-- EXAMPLE(UPTO 12AM MORNING SHIFT, 12-17 AFTERNOON , ELSE EVENING)
WITH hourly_sales 
AS (
SELECT *,
CASE 
    WHEN EXTRACT(HOUR from sale_time) < 12 THEN 'Morning'
	WHEN EXTRACT(HOUR from sale_time) BETWEEN 12 AND 17 THEN 'Afternoon'
	ELSE 'Evening'
	END as shift
FROM retail_sales
) 
SELECT shift,
COUNT(*) as total_sales
FROM hourly_sales 
GROUP BY shift;

